class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int num1, int num2)
    {
        int i=0;
        while(i<=32)
        {
            int a=(1<<i)&num1;
            int b=(1<<i)&num2;
            if(a!=b)
            return (i+1);

            i++;
        }
       
        return -1;

    }
};

